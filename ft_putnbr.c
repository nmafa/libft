#include "libft.h"

void	ft_putnbr(int n)
{
	int a;
	int z;

	a = n;
	z = 1;
	if (a < 0)
	{
		a = a * -1;
		ft_putchar('-');
		n = n * -1;
	}
	while (a > 9)
	{
		z *= 10;
		a /= 10;
	}
	a = n;
	while (z > 0)
	{
		a = ((n / z) % 10) + 48;
		ft_putchar(a);
		z /= 10;
	}
	
}
