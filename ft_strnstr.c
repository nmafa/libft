#include "libft.h"

char	*ft_strnstr(const char *s1, const char *s2, size_t len)
{
	size_t	i;
	size_t	n;

	i = 0;
	while (i < n && s1[i] != '\0')
	{
		if (s2[n] == '\0')
			return ((char *)s1 + 1);
		i++;
	}
	return (NULL);
}
