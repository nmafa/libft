#include "libft.h"

int		ft_atoi(const char *str)
{
	unsigned long	ret;
	long			neg;
	unsigned int	i;

	ret = 0;
	neg = 1;
	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
			str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i++] == '-')
			neg = -1;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		ret = ret * 10 + str[i] - '0';
		i++;
		if (ret > 9223372036854775807 && neg == 1)
			return (-1);
		if (ret > 9223372036854775807 && neg == -1)
			return (0);
	}
	return ((int)(ret * neg));
}
